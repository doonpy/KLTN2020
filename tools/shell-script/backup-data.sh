#!/bin/bash

MONGO_DATABASE="kltn2020"
APP_NAME="pk2020"
MONGO_HOST="159.65.0.142"
MONGO_PORT="27017"
MONGO_USERNAME="alice"
MONGO_PASSWORD="alice"

TIMESTAMP=`date +%F-%H%M`
MONGODUMP_PATH="mongodump"
BACKUPS_DIR=db-backup/$APP_NAME
BACKUP_NAME=$APP_NAME-$TIMESTAMP
d
echo "$MONGODUMP_PATH"
$MONGODUMP_PATH --host ${MONGO_HOST} --port ${MONGO_PORT} -d ${MONGO_DATABASE} -u ${MONGO_USERNAME} -p ${MONGO_PASSWORD} -v

mkdir -p ${BACKUPS_DIR}
mv dump ${BACKUP_NAME}
tar -zcvf ${BACKUPS_DIR}/${BACKUP_NAME}.tgz ${BACKUP_NAME}
rm -rf ${BACKUP_NAME}